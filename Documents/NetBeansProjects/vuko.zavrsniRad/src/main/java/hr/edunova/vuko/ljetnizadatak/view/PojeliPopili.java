    private void lstPodaciValueChanged(javax.swing.event.ListSelectionEvent evt) {//GEN-FIRST:event_lstPodaciValueChanged
         if (evt.getValueIsAdjusting()) {
            return;
        }

        entitet = lstPodaci.getSelectedValue();
        if (entitet == null) {
            return;
        }
        
        txtKolicina.setText(entitet.getKolicina());
        DefaultComboBoxModel<Karta> ku = (DefaultComboBoxModel<Karta>) cmbKarta.getModel();
        for (int i = 0; i < ku.getSize(); i++) {
            if (ku.getElementAt(i).getId().equals(entitet.getKarta().getId())) {
                cmbKarta.setSelectedIndex(i);
                break;
            }
        }
        
        DefaultComboBoxModel<HranaPice> hp = (DefaultComboBoxModel<HranaPice>) cmbHranaPice.getModel();
        for (int i = 0; i < hp.getSize(); i++) {
            if (ku.getElementAt(i).getId().equals(entitet.getHranaPice().getId())) {
                cmbHranaPice.setSelectedIndex(i);
                break;
            }
        }
        
    }//GEN-LAST:event_lstPodaciValueChanged

    private void btnDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDodajActionPerformed
         lblPoruka.setText("");
        entitet = new PojeoPopio();

        postaviVrijednostiUEntitet();

        try {
            obrada.create();
            ucitajPodatke();
        } catch (ZavrsniException ex) {
            lblPoruka.setText(ex.getPoruka());
        }
    }//GEN-LAST:event_btnDodajActionPerformed

    private void btnPromijeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPromijeniActionPerformed
        entitet = lstPodaci.getSelectedValue();
        if (entitet == null) {
            return;
        }

        postaviVrijednostiUEntitet();
        try {
            obrada.update();
            ucitajPodatke();

        } catch (ZavrsniException e) {
            lblPoruka.setText(e.getPoruka());
        }
    }//GEN-LAST:event_btnPromijeniActionPerformed

    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed
       entitet = lstPodaci.getSelectedValue();
        if (entitet == null) {
            return;
        }

        obrada.setEntitet(entitet);

        try {
            obrada.delete();
            ucitajPodatke();
        } catch (ZavrsniException e) {
            lblPoruka.setText(e.getPoruka());
        }
    }//GEN-LAST:event_btnObrisiActionPerformed

