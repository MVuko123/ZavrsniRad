    private void ucitajPodatke() {
         DefaultListModel<PojeoPopio> pp = new DefaultListModel<>();
        obrada.getPodaci().forEach( s-> pp.addElement(s));
        lstPodaci.setModel(pp);
    }

    private void postaviVrijednostiUEntitet() {
        entitet.setKolicina(txtKolicina.getText());
        entitet.setKarta((Karta) cmbKarta.getSelectedItem());
        entitet.setHranaPice((HranaPice) cmbHranaPice.getSelectedItem());
    }
